package butcher;

public class KindOfMeat {

	private int beefPrice;
	private int porkPrice;
	private int lambPrice;

	public KindOfMeat() {
		beefPrice = 10000;
		porkPrice = 5000;
		lambPrice = 3000;
	}

	public int getBeefPrice() {
		return beefPrice;
	}

	public int getPorkPrice() {
		return porkPrice;
	}

	public int getLambPrice() {
		return lambPrice;
	}

}
